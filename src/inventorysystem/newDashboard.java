    public void fetchdata() {

        int count = 0;
        try {

            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory_project", "root", "");
            Statement stmt = con.createStatement();

            ResultSet datas = stmt.executeQuery("select * from products");
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            while (datas.next()) {
                count = 1;
                model.addRow(new Object[]{datas.getInt("id"), datas.getString("name"), datas.getString("bought"), datas.getString("sold")});
            }
            if (count == 0) {
                JOptionPane.showMessageDialog(null, "No data found!.", "Alert", JOptionPane.WARNING_MESSAGE);
            }

//            fetch data from users_info
            datas = stmt.executeQuery("SELECT * FROM users_info");
            model = (DefaultTableModel) jTable2.getModel();

            while (datas.next()) {
                count = 1;
                model.addRow(new Object[]{datas.getInt("id"), datas.getString("email"), datas.getString("password"), datas.getString("name")});
            }
            if (count == 0) {
                JOptionPane.showMessageDialog(null, "No data found!.", "Alert", JOptionPane.WARNING_MESSAGE);
            }

            //Fetch data from products
            datas = stmt.executeQuery("SELECT * FROM products");
            model = (DefaultTableModel) jTable5.getModel();

            while (datas.next()) {
                count = 1;
                model.addRow(new Object[]{datas.getInt("id"), datas.getString("name"), datas.getInt("bought"), datas.getInt("sold")});
            }
            if (count == 0) {
                JOptionPane.showMessageDialog(null, "No data found!.", "Alert", JOptionPane.WARNING_MESSAGE);
            }
            //fetch data from users
            datas = stmt.executeQuery("SELECT * FROM users");
            model = (DefaultTableModel) jTable7.getModel();

            while (datas.next()) {
                count = 1;
                model.addRow(new Object[]{datas.getInt("id"), datas.getString("username"), datas.getString("password")});
            }
            if (count == 0) {
                JOptionPane.showMessageDialog(null, "No data found!.", "Alert", JOptionPane.WARNING_MESSAGE);
            }

            //fetch data from deactivated accounts
            datas = stmt.executeQuery("SELECT * FROM deactivate");
            model = (DefaultTableModel) jTable6.getModel();

            while (datas.next()) {
                count = 1;
                model.addRow(new Object[]{datas.getInt("id"), datas.getString("username"), datas.getString("password")});
            }
            if (count == 0) {
                JOptionPane.showMessageDialog(null, "No data found!.", "Alert", JOptionPane.WARNING_MESSAGE);
            }

            //fetch data from archived products
            datas = stmt.executeQuery("SELECT * FROM archive_products");
            model = (DefaultTableModel) jTable8.getModel();

            while (datas.next()) {
                count = 1;
                model.addRow(new Object[]{datas.getInt("id"), datas.getString("name"), datas.getInt("bought"), datas.getInt("sold")});
            }
            if (count == 0) {
                JOptionPane.showMessageDialog(null, "No data found!.", "Alert", JOptionPane.WARNING_MESSAGE);
            }

            //Fetch data from Customers
            datas = stmt.executeQuery("SELECT * FROM customers_info");
            model = (DefaultTableModel) jTable4.getModel();

            while (datas.next()) {
                count = 1;
                model.addRow(new Object[]{datas.getInt("id"), datas.getString("name"), datas.getString("email"), datas.getString("current_orders"), datas.getString("shipping_address")});
            }
            if (count == 0) {
                JOptionPane.showMessageDialog(null, "No data found!.", "Alert", JOptionPane.WARNING_MESSAGE);
            }

//            count number of products
            datas = stmt.executeQuery("select count(*) from products");
            datas.next();
            int countProducts = datas.getInt(1);
            countProduct.setText(String.valueOf(countProducts));

            //count the sold products
            datas = stmt.executeQuery("select SUM(sold) from products");
            datas.next();
            int countSold = datas.getInt(1);
            countSolds.setText(String.valueOf(countSold));

            //total products bought
            datas = stmt.executeQuery("select SUM(bought) from products");
            datas.next();
            int countbought = datas.getInt(1);
//            System.out.println(countbought);
            countBoughts.setText(String.valueOf(countbought));

            //available products
            proAvailable.setText(String.valueOf(countbought - countSold));

            con.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }


