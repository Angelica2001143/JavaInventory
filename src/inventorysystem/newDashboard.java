    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        // TODO add your handling code here:
        DefaultTableModel data = (DefaultTableModel) jTable3.getModel();
        int selectedIndex = jTable3.getSelectedRow();
        int id = Integer.parseInt(data.getValueAt(selectedIndex, 0).toString());

        String Names = name.getText();
        String Emails = email.getText();
        String Passwords = password.getText();
        String Usernames = username.getText();

        //creating a try catch block
        try {

            String query = "UPDATE `users_info` SET `name` = ? , `email` = ?,`password` = ?,`u_name` = ? WHERE `id` = ?";
            st = My_Connection.getConnection().prepareStatement(query);

            //updating data to the table
            st.setString(1, Names);
            st.setString(2, Emails);
            st.setString(3, Passwords);
            st.setString(4, Usernames);
            st.setInt(5, id);

            //to update the row
            st.executeUpdate();

            //to show a message to the user
            JOptionPane.showMessageDialog(this, "Updated Successfully!");
            display();

            //clearing the textfield
            name.setText("");
            email.setText("");
            password.setText("");
            username.setText("");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }


    }//GEN-LAST:event_updateMouseClicked

